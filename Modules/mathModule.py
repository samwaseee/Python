#            Build-in Math Functions

x = abs(-7.25)
print(x)  # 7.25
x = pow(4, 3)
print(x)  # 64
x = max(5, 10)
print(x)  # 10
x = min(5, 10)
print(x)  # 5
x = round(5.7)
print(x)  # 6

#           Math Module

import math

x = math.sqrt(64)
print(x)  # 8.0

x = math.ceil(1.4)
print(x)  # 2

x = math.floor(1.4)
print(x)  # 1

x = math.pi
print(x)  # 3.141592653589793

x = math.e
print(x)  # 2.718281828459045

x = math.tau
print(x)  # 6.283185307179586

x = math.inf
print(x)  # inf

x = math.nan
print(x)  # nan

x = math.factorial(5)
print(x)  # 120

x = math.gcd(12, 15)
print(x)  # 3

x = math.lcm(12, 15)
print(x)  # 60

x = math.log(100, 10)
print(x)  # 2.0

x = math.log2(8)
print(x)  # 3.0

x = math.log10(100)
print(x)  # 2.0

x = math.sin(math.pi / 2)
print(x)  # 1.0 
