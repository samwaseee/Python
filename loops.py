for x in range(2, 30, 3):  # Looping through a range of numbers
    print(x)  # Printing the current number in the loop
else:
    print("Loop completed")  # Printing a message after the loop is completed

i = 1
while i < 6:  # Looping while i is less than 6
    print(i)  # Printing the current value of i
    i += 1  # Incrementing i by 1
else:
    print("i is no longer less than 6")